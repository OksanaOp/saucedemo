/*
5. Порівняння чисел
Порівняйте два числа: виведіть більше, або повідомте, що числа рівні.
Вхід: Два числа (наприклад, 8 і 10)
Вихід:
- "Перше число більше."
- "Друге число більше."
- "Числа рівні."
*/

export function compareNumbers(a: number, b: number) {
  if (a === b) {
    return "Числа рівні.";
  } else if (a > b) {
    return "Перше число більше.";
  } else if (a < b) {
    return "Друге число більше.";
  } else {
    throw new Error("");
  }
}
