/* 1. 
Парне чи непарне число
Напишіть програму, яка визначає, чи число парне або непарне.
Вхід: Число (наприклад, 4)
Вихід:
- "Число парне."
- "Число непарне."
*/

export function isEven(number: number) {
  if (typeof number === "number") {
    if (number % 2 === 0) {
      return "Число парне.";
    } else {
      return "Число непарне.";
    }
  }
}
